    public void tabelaGUI(){
        
    try{
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("crud");
        EntityManager em = emf.createEntityManager();
        
        
        List<Cliente> clientes = em.createQuery("select c from Cliente c ", Cliente.class).getResultList();
        
        DefaultTableModel model = (DefaultTableModel) tableClientes.getModel();
        model.setNumRows(0);
        
        for(Cliente cliente1 : clientes){
        
            Object[] linhasLista = {
            
                cliente1.getId(),
                cliente1.getNome(),
                cliente1.getCpf(),
                cliente1.getTelefone(),
                cliente1.getSexo(),
                cliente1.getEndereco(),
                cliente1.getCidade(),
                cliente1.getCep()};
            model.addRow(linhasLista);
            tableClientes.setModel(model);
            
        }
        
    }
    catch(Exception e1){JOptionPane.showMessageDialog(null, "Servidor Sem Conex√£o!");}
    }

    
